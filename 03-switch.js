// Switch Case

let friend = 'jeff';

switch (friend.toLowerCase()) {
    case 'zach':
        console.log('Let us go play ultimate!')
        break;
    case
        'ing':
        console.log('So...are you taking me home?')
        break;
    case
        'adam':
        console.log('I probably have baby stuff')
        break;
        default:
        console.log(`you, ${friend}, are not my friend`)
}


